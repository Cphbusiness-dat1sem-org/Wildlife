DROP DATABASE IF EXISTS `wildlife`;
CREATE SCHEMA `wildlife`;
USE `wildlife`;

DROP TABLE IF EXISTS `classes`;
CREATE TABLE `classes` (
  `class` VARCHAR(45) NOT NULL,
  PRIMARY KEY (`class`));

DROP TABLE IF EXISTS `animals`;
CREATE TABLE `animals` (
  `animal_name` VARCHAR(45) NOT NULL,
  `class` VARCHAR(45) NOT NULL,
  PRIMARY KEY (`animal_name`, `class`),
  CONSTRAINT `fk_animals`
  FOREIGN KEY (`class`)
  REFERENCES `classes` (`class`));

DROP TABLE IF EXISTS `groups`;
CREATE TABLE `groups` (
  `animal_name` VARCHAR(45) NOT NULL,
  `group` VARCHAR(45) NOT NULL,
  PRIMARY KEY (`group`, `animal_name`),
  CONSTRAINT `fk_groups`
  FOREIGN KEY (`animal_name`)
  REFERENCES `animals` (`animal_name`));

DROP TABLE IF EXISTS `translations`;
CREATE TABLE `translations` (
  `animal_name` VARCHAR(45) NOT NULL,
  `danish` VARCHAR(45) NULL,
  PRIMARY KEY (`animal_name`),
  CONSTRAINT `fk_translate`
  FOREIGN KEY (`animal_name`)
  REFERENCES `animals` (`animal_name`));
  
  
  
